    private void ModifyUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifyUserButtonActionPerformed
        User user = resSystem.searchUsers(Integer.parseInt(UserIDField.getText()));
        if(AdminCheck.isSelected())
        {
        user.setName(NameField.getText());
        user.setEmail(EMailField.getText());
        user.setPassword(PasswordField.getPassword());
        user.setPrivliges(CheckFaculty.isSelected());
        user.setPhone(PhoneField.getText());
        }
        else
        {
        user.setName(NameField.getText());
        user.setEmail(EMailField.getText());
        user.setPassword(PasswordField.getPassword());
        user.setPrivliges(CheckFaculty.isSelected());
        }
        
        ReservationSystem.save(resSystem);
        JOptionPane.showMessageDialog(null, "User Modified");
        this.setVisible(false);
        
    }//GEN-LAST:event_ModifyUserButtonActionPerformed

