    private void FindUserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_FindUserButtonActionPerformed
        
        if(!UserIDField.getText().isEmpty()){
        if(resSystem.searchUsers(Integer.parseInt(UserIDField.getText())) == null)
        {
            UserNotFound.setVisible(true);
            NameField.setText(null);
        EMailField.setText(null);
        DepartmentField.setText(null);
        CheckFaculty.setSelected(false);
        AdminCheck.setSelected(false);
        PhoneField.setText(null);
        }
        else
        {
        User user = resSystem.searchUsers(Integer.parseInt(UserIDField.getText()));
        UserNotFound.setVisible(false);
        NameField.setText(user.getUserName());
        EMailField.setText(user.getUserEmail());
        DepartmentField.setText(user.getDepartment());
        if(user.hasPriviliges())
            CheckFaculty.setSelected(true);
        if(user instanceof Administrator)
        {
            AdminCheck.setSelected(true);
            PhoneField.setVisible(true);
            PhoneLabel.setVisible(true);
            PhoneField.setText(String.valueOf(user.getPhone()));
            DepartmentField.setText("Warrington IT");
        }
        else
        {
            AdminCheck.setVisible(false);
            PhoneField.setVisible(false);
            PhoneLabel.setVisible(false);
        }}}
        else
        JOptionPane.showMessageDialog(null, "Please enter a valid UserID");
    }//GEN-LAST:event_FindUserButtonActionPerformed

